import UIKit

extension UIColor {
    
    //#FBFBFB
    class var appBackgroundColor : UIColor {
        return #colorLiteral(red: 0.9843137255, green: 0.9843137255, blue: 0.9843137255, alpha: 1)
    }
    
    //#FF6604
    class var primaryColor : UIColor {
        return #colorLiteral(red: 0, green: 0.5854354501, blue: 1, alpha: 1)
    }
    
    class var bgColor : UIColor {
        return #colorLiteral(red: 0.9764705882, green: 0.9764705882, blue: 0.9764705882, alpha: 1)
    }
    
    class var searchBtnBgColor : UIColor {
        return #colorLiteral(red: 0.9764705882, green: 0.9764705882, blue: 0.9764705882, alpha: 1)
    }
    
    class var appLightGray : UIColor {
        return #colorLiteral(red: 0.5058823529, green: 0.5058823529, blue: 0.5058823529, alpha: 1)
         
    }
    
    class var successColor: UIColor {
        return #colorLiteral(red: 0.4666666687, green: 0.7647058964, blue: 0.2666666806, alpha: 1)
    }
    
    class var errorColor: UIColor {
        return #colorLiteral(red: 0.9254902005, green: 0.2352941185, blue: 0.1019607857, alpha: 1)
    }
    
}
